public class Main {
    public static void main(String[] args)
    {
        int numToExamine = 4; // Initialize the variable to use for even or odd testing
        if (numToExamine % 2 == 0) { // Condition: If the remainder of the number divided by 2 is 0
            System.out.println("Even"); // Output even if the condition is true
        } else {
            System.out.println("Odd"); // Output odd if condition is not true
        }
    }
}